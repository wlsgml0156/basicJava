package oop1210;

public class Test02_print {
  public static void main(String[] args) {
    // overloading 중복정의
    // + 연산자
    System.out.println(10+20);  //더하기
    System.out.println(10+"20");//연결하기
    System.out.println("10"+20);
    System.out.println("10"+"20");
    System.out.println("10+20");   
    
    // " ' \등을 단순 기호로 인식하고자 할때
    System.out.println("\"");
    System.out.println("\'");
    System.out.println("\\");
      
    
  }//main() end
}//class end
